public class TypesData {
    public static void main(String[] args) {

        // Types Data Java
        int myNum = 15; // integer
        float myFloat = 5.99F; // Float
        char myLetter = 'D'; // char / character
        boolean myBoolean = true; // boolean
        String myText = "Hello World"; // String harus menggunakan petik dua (")

        System.out.println(myNum);
        System.out.println(myFloat);
        System.out.println(myLetter);
        System.out.println(myBoolean);
        System.out.println(myText);

        //Type data primitif
        byte myByte = 100;
        System.out.println(myByte);

        short myShort = 10000;
        System.out.println(myShort);

        int myInt = 10000000;
        System.out.println(myInt);

        long myLong = 10000000000L;
        System.out.println(myLong);

        float myFloat1 = 5.75f;
        System.out.println(myFloat1);

        double myDouble = 19.99d;
        System.out.println(myDouble);

        float fl = 35e3f;
        System.out.println(fl);

        double dl = 12E4d;
        System.out.println(dl);

        boolean isJavaFun = true;
        boolean isJavaSaid = false;
        System.out.println(isJavaFun);
        System.out.println(isJavaSaid);

        char myGrade = 'A';
        System.out.println(myGrade);

        char a = 65, b = 66, c = 67;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        String greeting = "Hello World";
        System.out.println(greeting);

    }
}
